  // Initialize LUT files on first use using proper 3D LUT generator
  private static async ensureLUTFiles(): Promise<void> {
    // If generation is already in progress, wait for it
    if (this.lutGenerationLock) {
      console.log('[VideoProcessor] Waiting for ongoing LUT generation...');
      await this.lutGenerationLock;
      return;
    }

    const { generateAllLUTs, getLUTPresets } = await import('./lut-generator');

    // CRITICAL SECTION: Re-check lock after import (prevents race condition)
    if (this.lutGenerationLock) {
      console.log('[VideoProcessor] Lock acquired by concurrent request during import, waiting...');
      await this.lutGenerationLock;
      return;
    }

    // Get canonical list of LUT presets from generator
    const allLUTPresets = getLUTPresets();

    // Check if ALL LUT files exist
    const allExist = allLUTPresets.every(name =>
      fs.existsSync(path.join(LUTS_DIR, `${name}.cube`))
    );

    if (!allExist) {
      // Atomically acquire lock (if still null)
      if (this.lutGenerationLock) {
        console.log('[VideoProcessor] Lock acquired between checks, waiting...');
        await this.lutGenerationLock;
        return;
      }

      // Set lock and begin generation
      this.lutGenerationLock = (async () => {
        try {
          // Double-check pattern: verify files still missing inside lock
          const stillMissingBeforeGen = allLUTPresets.filter(name =>
            !fs.existsSync(path.join(LUTS_DIR, `${name}.cube`))
          );

          if (stillMissingBeforeGen.length === 0) {
            // Another request completed generation while we waited
            console.log('[VideoProcessor] LUTs already generated by concurrent request');
            return;
          }

          console.log(`[VideoProcessor] Generating ${stillMissingBeforeGen.length} missing 3D LUTs...`);

          generateAllLUTs(); // Synchronous blocking call - generates all LUTs before returning

          // Revalidate that all files were created successfully
          const stillMissing = allLUTPresets.filter(name =>
            !fs.existsSync(path.join(LUTS_DIR, `${name}.cube`))
          );

          if (stillMissing.length > 0) {
            throw new Error(`LUT generation incomplete: missing ${stillMissing.join(', ')}`);
          }

          console.log(`[VideoProcessor] LUT generation complete - all ${allLUTPresets.length} presets ready`);
        } catch (error) {
          console.error('[VideoProcessor] LUT generation failed:', error);
          throw new Error(`Failed to generate LUT files: ${error instanceof Error ? error.message : String(error)}`);
        } finally {
          // Always release lock, even on early return
          this.lutGenerationLock = null;
        }
      })();

      await this.lutGenerationLock;
    }
  }

  // Get video metadata with caching (cache invalidates if file changes)
  static async getVideoMetadata(videoPath: string): Promise<{ duration: number; width: number; height: number; metadata: any }> {
    // Create cache key with file stats to detect changes
    const stats = fs.statSync(videoPath);
    const cacheKey = `${videoPath}:${stats.size}:${stats.mtimeMs}`;

    if (metadataCache.has(cacheKey)) {
      console.log(`[VideoProcessor] Using cached metadata for ${path.basename(videoPath)}`);
      return metadataCache.get(cacheKey)!;
    }

    return new Promise((resolve, reject) => {
      ffmpeg.ffprobe(videoPath, (err: any, metadata: any) => {
        if (err) {
          reject(err);
          return;
        }
        const duration = metadata.format.duration || 0;
        const videoStream = metadata.streams.find((s: any) => s.codec_type === 'video');
        const width = videoStream?.width || 1920;
        const height = videoStream?.height || 1080;

        const cachedData = { duration: Math.floor(duration), width, height, metadata };
        metadataCache.set(cacheKey, cachedData);
        console.log(`[VideoProcessor] Cached metadata for ${path.basename(videoPath)}: ${duration}s, ${width}x${height}`);
        resolve(cachedData);
      });
    });
  }
  static async downloadFromUrl(url: string): Promise<{
    videoPath: string;
    duration: number;
  }> {
    const videoId = nanoid();
    const videoPath = path.join(VIDEOS_DIR, `${videoId}.mp4`);

    // Check if it's a YouTube URL
    if (url.includes("youtube.com") || url.includes("youtu.be")) {
      return this.downloadYouTubeVideo(url);
    }

    // For other URLs (Google Drive, Dropbox, direct URLs), use generic download
    return this.downloadGenericUrl(url, videoPath);
  }

  private static async downloadYouTubeVideo(url: string): Promise<{
    videoPath: string;
    duration: number;
  }> {
    const videoId = nanoid();
    const videoPath = path.join(VIDEOS_DIR, `${videoId}.mp4`);

    return new Promise((resolve, reject) => {
      const stream = ytdl(url, {
        quality: "highest",
        filter: "audioandvideo",
      });

      stream.pipe(fs.createWriteStream(videoPath));

      stream.on("end", async () => {
        try {
          // Use cached metadata method
          const { duration } = await this.getVideoMetadata(videoPath);
          resolve({ videoPath, duration });
        } catch (err) {
          reject(err);
        }
      });

      stream.on("error", reject);
    });
  }

  private static async downloadGenericUrl(
    url: string,
    videoPath: string
  ): Promise<{
    videoPath: string;
    duration: number;
  }> {
    // For Google Drive shareable links, convert to direct download
    let downloadUrl = url;
    if (url.includes("drive.google.com")) {
      const fileId = url.match(/\/d\/([^/]+)/)?.[1] || url.match(/id=([^&]+)/)?.[1];
      if (fileId) {
        downloadUrl = `https://drive.google.com/uc?export=download&id=${fileId}`;
      }
    }

    return new Promise((resolve, reject) => {
      const protocol = downloadUrl.startsWith("https") ? https : http;

      protocol.get(downloadUrl, (response: any) => {
        if (response.statusCode === 302 || response.statusCode === 301) {
          // Follow redirect
          return this.downloadGenericUrl(response.headers.location, videoPath)
            .then(resolve)
            .catch(reject);
        }

        const file = fs.createWriteStream(videoPath);
        response.pipe(file);

        file.on("finish", async () => {
          file.close();
          try {
            // Use cached metadata method
            const { duration } = await this.getVideoMetadata(videoPath);
            resolve({ videoPath, duration });
          } catch (err) {
            reject(err);
          }
        });
      }).on("error", (err: any) => {
        fs.unlink(videoPath, () => {}); // Delete the file if error
        reject(err);
      });
    });
  }

