  // AUTO-TRIM FILLER WORDS - Remove "um", "uh", "like", "you know"
  static detectFillerWords(segments: Array<{ start: number; end: number; text: string }>): Array<{ start: number; end: number }> {
    const fillerWords = ['um', 'uh', 'like', 'you know', 'i mean', 'basically', 'actually', 'literally'];
    const fillerSegments: Array<{ start: number; end: number }> = [];

    for (const segment of segments) {
      const words = segment.text.toLowerCase().split(/\s+/);
      const segmentDuration = segment.end - segment.start;
      const timePerWord = segmentDuration / words.length;

      words.forEach((word, index) => {
        const cleanWord = word.replace(/[.,!?]/g, '');
        if (fillerWords.includes(cleanWord)) {
          const wordStart = segment.start + (index * timePerWord);
          const wordEnd = wordStart + timePerWord;
          fillerSegments.push({ start: wordStart, end: wordEnd });
          console.log(`[AutoTrim] Detected filler word "${cleanWord}" at ${wordStart.toFixed(2)}s - ${wordEnd.toFixed(2)}s`);
        }
      });
    }

    return fillerSegments;
  }

  // Remove filler word segments from video
  static async removeFillerWords(
    videoPath: string,
    fillerSegments: Array<{ start: number; end: number }>
  ): Promise<string> {
    if (fillerSegments.length === 0) {
      console.log(`[AutoTrim] No filler words detected, returning original video`);
      return videoPath;
    }

    const outputId = nanoid();
    const outputPath = path.join(VIDEOS_DIR, `autotrim-${outputId}.mp4`);

    // Get video duration
    const { duration } = await this.getVideoMetadata(videoPath);

    // Create segments to KEEP (inverse of filler segments)
    const keepSegments: Array<{ start: number; end: number }> = [];
    let currentStart = 0;

    // Sort filler segments by start time
    const sortedFillers = fillerSegments.sort((a, b) => a.start - b.start);

    for (const filler of sortedFillers) {
      if (currentStart < filler.start) {
        keepSegments.push({ start: currentStart, end: filler.start });
      }
      currentStart = filler.end;
    }

    // Add final segment if there's content after last filler
    if (currentStart < duration) {
      keepSegments.push({ start: currentStart, end: duration });
    }

    console.log(`[AutoTrim] Removing ${fillerSegments.length} filler segments, keeping ${keepSegments.length} segments`);

    // Extract and concatenate keep segments
    const tempClips: string[] = [];
    for (let i = 0; i < keepSegments.length; i++) {
      const segment = keepSegments[i];
      const clipPath = await this.extractClip(videoPath, segment.start, segment.end);
      tempClips.push(clipPath);
    }

    // Concatenate all kept segments
    await this.concatenateClips(tempClips, outputPath);

    // Cleanup temp clips
    for (const clip of tempClips) {
      try {
        fs.unlinkSync(clip);
      } catch (err) {
        console.warn(`[AutoTrim] Failed to delete temp clip: ${clip}`);
      }
    }

    console.log(`[AutoTrim] Successfully removed filler words: ${path.basename(outputPath)}`);
    return outputPath;
  }

  static async cropToAspectRatio(
    videoPath: string,
    aspectRatio: "9:16" | "1:1" | "16:9"
  ): Promise<string> {
    const outputId = nanoid();
    const outputPath = path.join(VIDEOS_DIR, `cropped-${outputId}.mp4`);

    // Calculate crop filter based on aspect ratio
    const cropFilters: Record<string, string> = {
      "9:16": "crop=ih*9/16:ih", // Vertical: crop width to 9/16 of height
      "1:1": "crop=min(iw\\,ih):min(iw\\,ih)", // Square: crop to smallest dimension
      "16:9": "crop=iw:iw*9/16", // Landscape: crop height to 9/16 of width
    };

    const scaleFilters: Record<string, string> = {
      "9:16": "scale=1080:1920", // Standard vertical resolution
      "1:1": "scale=1080:1080", // Standard square resolution
      "16:9": "scale=1920:1080", // Standard landscape resolution
    };

    const filter = `${cropFilters[aspectRatio]},${scaleFilters[aspectRatio]}`;

    return new Promise((resolve, reject) => {
      ffmpeg(videoPath)
        .videoFilters(filter)
        .output(outputPath)
        .videoCodec("libx264")
        .audioCodec("aac")
        .outputOptions([
          '-preset', 'ultrafast',
          '-threads', '0',  // PERFORMANCE: Fast encoding without quality loss
          '-crf', '20'  // Constant Rate Factor: 20 for high quality
        ])
        .on("end", () => resolve(outputPath))
        .on("error", reject)
        .run();
    });
  }

  // Apply Ken Burns motion effect (slow zoom + pan)
  static async applyKenBurnsEffect(
    videoPath: string,
    intensity: "subtle" | "moderate" | "dramatic" = "moderate"
  ): Promise<string> {
    const outputId = nanoid();
    const outputPath = path.join(VIDEOS_DIR, `kenburns-${outputId}.mp4`);

    console.log(`[KenBurns] Applying ${intensity} effect to video`);

    // Get video metadata for scale calculations
    const { duration, width, height } = await this.getVideoMetadata(videoPath);

    // Ken Burns intensity presets
    const presets = {
      subtle: { startZoom: 1.0, endZoom: 1.05, panX: 0, panY: 10 }, // 5% zoom, slight down pan
      moderate: { startZoom: 1.0, endZoom: 1.1, panX: 20, panY: 20 }, // 10% zoom, diagonal pan
      dramatic: { startZoom: 1.0, endZoom: 1.2, panX: 40, panY: 30 }, // 20% zoom, strong pan
    };

    const preset = presets[intensity];
    const framerate = 60; // Assume 60fps for smooth motion

    // Calculate zoom/pan trajectory
    // zoompan filter: zoom factor increases over time, with X/Y pan
    const zoomFilter = `zoompan=z='min(zoom+0.0015,${preset.endZoom})':x='iw/2-(iw/zoom/2)+${preset.panX}*sin(on/${framerate})':y='ih/2-(ih/zoom/2)+${preset.panY}*cos(on/${framerate})':d=${duration * framerate}:s=${width}x${height}:fps=${framerate}`;

    return new Promise((resolve, reject) => {
      ffmpeg(videoPath)
        .videoFilters(zoomFilter)
        .output(outputPath)
        .videoCodec("libx264")
        .audioCodec("copy")
        .outputOptions([
          '-preset', 'ultrafast',
          '-threads', '0',
          '-crf', '20',
        ])
        .on("end", () => {
          console.log(`[KenBurns] Effect applied: ${outputPath}`);
          resolve(outputPath);
        })
        .on("error", (err) => {
          console.error(`[KenBurns] Error:`, err.message);
          reject(err);
        })
        .run();
    });
  }

