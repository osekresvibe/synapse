  static async createTransition(
    clip1Path: string,
    clip2Path: string,
    transitionType: "cut" | "fade" | "dissolve" | "zoom" = "cut",
    engagementScore?: number // NEW: Use engagement to determine transition intensity
  ): Promise<string> {
    const outputId = nanoid();
    const outputPath = path.join(VIDEOS_DIR, `transition-${outputId}.mp4`);

    if (transitionType === "cut") {
      // Simple concatenation for jump cuts
      await this.concatenateClips([clip1Path, clip2Path], outputPath);
      return outputPath;
    }

    // Use ZOOM transition for high engagement clips (>85 score)
    if (transitionType === "zoom" || (engagementScore && engagementScore > 85)) {
      return new Promise((resolve, reject) => {
        ffmpeg()
          .input(clip1Path)
          .input(clip2Path)
          .complexFilter([
            // Zoom out from clip1 while fading
            `[0:v]scale=iw*1.2:ih*1.2,fade=out:st=0:d=0.3[v0]`,
            // Zoom in to clip2 while fading
            `[1:v]scale=iw*0.8:ih*0.8,fade=in:st=0:d=0.3,scale=iw/0.8:ih/0.8[v1]`,
            // Overlay with crossfade
            `[v0][v1]xfade=transition=fade:duration=0.3:offset=0[v]`,
            `[0:a][1:a]acrossfade=d=0.3[a]`,
          ])
          .map("[v]")
          .map("[a]")
          .output(outputPath)
          .on("end", () => resolve(outputPath))
          .on("error", reject)
          .run();
      });
    }

    return new Promise((resolve, reject) => {
      // For fade/dissolve transitions
      const transitionDuration = transitionType === "fade" ? 0.5 : 1;

      ffmpeg()
        .input(clip1Path)
        .input(clip2Path)
        .complexFilter([
          `[0:v][1:v]xfade=transition=fade:duration=${transitionDuration}:offset=0[v]`,
          `[0:a][1:a]acrossfade=d=${transitionDuration}[a]`,
        ])
        .map("[v]")
        .map("[a]")
        .output(outputPath)
        .on("end", () => resolve(outputPath))
        .on("error", reject)
        .run();
    });
  }

