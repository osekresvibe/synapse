  // Helper to convert hex color to ASS format (&HAABBGGRR&)
  private static hexToAssColor(hexColor: string, opacity: number = 100): string {
    // Remove # if present
    const hex = hexColor.replace('#', '');
    // Extract RGB components
    const r = hex.substring(0, 2);
    const g = hex.substring(2, 4);
    const b = hex.substring(4, 6);
    // Calculate alpha (0-255, where 0 is opaque, 255 is transparent)
    const alpha = Math.round((100 - opacity) * 255 / 100);
    const alphaHex = alpha.toString(16).padStart(2, '0');
    // ASS format is &HAABBGGRR& (alpha, blue, green, red - reverse of RGB!)
    return `&H${alphaHex}${b}${g}${r}&`.toUpperCase();
  }

