  static async regenerateVideoFromClips(
    sourceVideoPath: string,
    slices: Array<{ id: string; startTime: number; endTime: number }>,
    clipSequence: string[],
    videoType: "short" | "standard" | "comprehensive",
    aspectRatio?: "9:16" | "1:1" | "16:9",
    refinedParams?: {
      transitionStyle?: string;
      pacing?: "slow" | "normal" | "fast";
      colorGrade?: string;
      enableJLCuts?: boolean;
      jlCutType?: "j-cut" | "l-cut" | "auto";
    }
  ): Promise<string> {
    console.log(`[regenerate] Creating ${videoType} video from ${clipSequence.length} clips (PARALLEL MODE)`);
    if (refinedParams) {
      console.log(`[regenerate] Using refined params:`, refinedParams);
    }

    const tempClipPaths: string[] = [];

    try {
      // PARALLEL PROCESSING: Extract all clips concurrently
      const clipSpecs = clipSequence
        .map(sliceId => {
          const slice = slices.find(s => s.id === sliceId);
          if (!slice) {
            console.warn(`[regenerate] Slice ${sliceId} not found, skipping`);
            return null;
          }
          return {
            startTime: slice.startTime,
            endTime: slice.endTime,
            id: sliceId
          };
        })
        .filter(spec => spec !== null) as Array<{ startTime: number; endTime: number; id: string }>;

      if (clipSpecs.length === 0) {
        throw new Error("No valid clips to extract");
      }

      // Extract all clips in parallel (3-5x faster than sequential)
      const extractedClips = await this.extractClipsParallel(
        sourceVideoPath,
        clipSpecs,
        { reEncode: false } // Use stream copy for speed
      );

      tempClipPaths.push(...extractedClips);

      // Create the final concatenated video
      const outputId = nanoid();
      const outputPath = path.join(VIDEOS_DIR, `${videoType}-${outputId}.mp4`);

      console.log(`[regenerate] Concatenating ${tempClipPaths.length} clips into ${outputPath}`);
      await this.concatenateClips(tempClipPaths, outputPath);

      // Apply aspect ratio cropping if specified
      let finalPath = outputPath;
      if (aspectRatio && aspectRatio !== "16:9") {
        console.log(`[regenerate] Applying aspect ratio: ${aspectRatio}`);
        finalPath = await this.cropToAspectRatio(outputPath, aspectRatio);
        // Clean up uncropped temp file
        try {
          fs.unlinkSync(outputPath);
        } catch (err) {
          console.warn(`[regenerate] Failed to delete uncropped temp file`);
        }
      }

      // Clean up temp clips
      for (const tempPath of tempClipPaths) {
        try {
          fs.unlinkSync(tempPath);
        } catch (err) {
          console.warn(`[regenerate] Failed to delete temp clip ${tempPath}:`, err);
        }
      }

      // Apply refined parameters if provided
      if (refinedParams) {
        // Apply color grading if specified
        if (refinedParams.colorGrade && refinedParams.colorGrade !== 'none') {
          console.log(`[regenerate] Applying refined color grade: ${refinedParams.colorGrade}`);
          const gradedPath = await this.applyColorGrading(finalPath, refinedParams.colorGrade);
          // Clean up non-graded version
          if (finalPath !== gradedPath) {
            try {
              fs.unlinkSync(finalPath);
            } catch (err) {
              console.warn(`[regenerate] Failed to delete non-graded temp file`);
            }
            finalPath = gradedPath;
          }
        }
      }

      console.log(`[regenerate] Video regenerated successfully: ${finalPath}`);
      return finalPath;
    } catch (error) {
      // Clean up temp clips on error
      for (const tempPath of tempClipPaths) {
        try {
          fs.unlinkSync(tempPath);
        } catch (err) {
          // Ignore cleanup errors
        }
      }
      throw error;
    }
  }

  static async exportWithFormatAndQuality(
    videoPath: string,
    format: string,
    quality: string
  ): Promise<string> {
    const outputId = nanoid();
    const fileExtension = format === "mov" ? "mov" : format === "webm" ? "webm" : "mp4";
    const outputPath = path.join(VIDEOS_DIR, `export-${outputId}.${fileExtension}`);

    const qualitySettings: Record<string, { width: number; height: number; bitrate: string }> = {
      high: { width: 1920, height: 1080, bitrate: "5000k" },
      medium: { width: 1280, height: 720, bitrate: "2500k" },
      low: { width: 854, height: 480, bitrate: "1000k" },
    };

    const settings = qualitySettings[quality] || qualitySettings.high;

    return new Promise((resolve, reject) => {
      const command = ffmpeg(videoPath)
        .size(`${settings.width}x${settings.height}`)
        .videoBitrate(settings.bitrate)
        .output(outputPath);

      if (format === "mp4") {
        command
          .videoCodec("libx264")
          .audioCodec("aac")
          .outputOptions([
            "-preset", "ultrafast",
            "-threads", "0",
            "-crf", "23",
            "-pix_fmt", "yuv420p"
          ]);
      } else if (format === "webm") {
        command
          .videoCodec("libvpx-vp9")
          .audioCodec("libopus")
          .outputOptions([
            "-deadline", "good",
            "-cpu-used", "2",
            "-row-mt", "1"
          ]);
      } else if (format === "mov") {
        command
          .videoCodec("prores_ks")
          .audioCodec("pcm_s16le")
          .outputOptions([
            "-profile:v", "2",
            "-vendor", "apl0",
            "-bits_per_mb", "8000",
            "-pix_fmt", "yuv422p10le"
          ]);
      }

      command
        .on("end", () => {
          console.log(`[export] Exported video: ${format} (${quality}) -> ${outputPath}`);
          resolve(outputPath);
        })
        .on("error", (err) => {
          console.error(`[export] Export failed:`, err);
          reject(err);
        })
        .run();
    });
  }
