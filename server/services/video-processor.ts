  static async extractClip(
    videoPath: string,
    startTime: number,
    endTime: number,
    options?: { reEncode?: boolean; ultraFast?: boolean }
  ): Promise<string> {
    const clipId = nanoid();
    const clipPath = path.join(VIDEOS_DIR, `clip-${clipId}.mp4`);

    console.log(`[extractClip] Extracting ${startTime}s-${endTime}s from ${path.basename(videoPath)} (reEncode: ${options?.reEncode ?? false}, ultraFast: ${options?.ultraFast ?? false})`);

    return new Promise((resolve, reject) => {
      const command = ffmpeg(videoPath)
        .setStartTime(startTime)
        .setDuration(endTime - startTime)
        .output(clipPath);

      if (options?.reEncode) {
        // Ultra-fast mode for speed (10x faster)
        if (options.ultraFast) {
          command.outputOptions([
            '-c:v', 'libx264',
            '-preset', 'veryfast', // Faster than ultrafast for better quality/speed ratio
            '-tune', 'zerolatency', // Optimize for speed
            '-threads', '0', // Use all CPU cores
            '-crf', '28', // Higher CRF for faster encoding (still good quality)
            '-c:a', 'copy', // Don't re-encode audio
            '-movflags', '+faststart',
            '-avoid_negative_ts', 'make_zero',
            '-fps_mode', 'passthrough' // Don't modify framerate
          ]);
        } else {
          command.outputOptions([
            '-c:v', 'libx264',
            '-preset', 'ultrafast',
            '-threads', '0',
            '-crf', '23',
            '-c:a', 'aac',
            '-b:a', '128k',
            '-movflags', '+faststart',
            '-avoid_negative_ts', 'make_zero'
          ]);
        }
      } else {
        command.outputOptions([
          '-c:v', 'copy',
          '-c:a', 'copy',
          '-movflags', '+faststart',
          '-avoid_negative_ts', 'make_zero'
        ]);
      }

      command
        .on("end", async () => {
          const stats = fs.statSync(clipPath);
          console.log(`[extractClip] Successfully extracted clip: ${clipId} (${(stats.size / 1024).toFixed(1)}KB)`);

          if (stats.size < 1000) {
            console.warn(`[extractClip] Warning: Clip ${clipId} is very small, may be corrupted`);
          }
          resolve(clipPath);
        })
        .on("error", (err) => {
          console.error(`[extractClip] Error extracting clip:`, err.message);
          reject(err);
        })
        .on("stderr", (stderrLine) => {
          console.log(`[extractClip] FFmpeg: ${stderrLine}`);
        })
        .run();
    });
  }

  // PARALLEL PROCESSING: Extract multiple clips concurrently
  static async extractClipsParallel(
    videoPath: string,
    clipSpecs: Array<{ startTime: number; endTime: number; id?: string }>,
    options?: { reEncode?: boolean },
    onProgress?: (completed: number, total: number) => void // Added onProgress callback
  ): Promise<string[]> {
    console.log(`[extractClipsParallel] Extracting ${clipSpecs.length} clips in parallel (max ${MAX_CONCURRENT_CLIPS} concurrent)`);
    const startTime = Date.now();

    const limit = pLimit(MAX_CONCURRENT_CLIPS);

    let completed = 0;
    const total = clipSpecs.length;

    const extractionPromises = clipSpecs.map((spec, index) =>
      limit(async () => {
        try {
          const clipPath = await this.extractClip(
            videoPath,
            spec.startTime,
            spec.endTime,
            options
          );
          completed++;
          if (onProgress) onProgress(completed, total); // Call progress callback
          console.log(`[extractClipsParallel] Completed clip ${index + 1}/${clipSpecs.length}`);
          return clipPath;
        } catch (error: any) {
          console.error(`[extractClipsParallel] Error extracting clip ${index + 1}:`, error.message);
          // Decide how to handle errors: re-throw, return null, or continue
          throw error; // Re-throwing to stop Promise.all if any clip fails
        }
      })
    );

    const clipPaths = await Promise.all(extractionPromises);
    const elapsed = ((Date.now() - startTime) / 1000).toFixed(1);
    console.log(`[extractClipsParallel] ✓ Extracted ${clipPaths.length} clips in ${elapsed}s (${(clipSpecs.length / parseFloat(elapsed)).toFixed(1)} clips/s)`);

    return clipPaths;
  }

  static async concatenateClips(
    clipPaths: string[],
    outputPath: string
  ): Promise<void> {
    const concatListPath = path.join(VIDEOS_DIR, `concat-${nanoid()}.txt`);

    console.log(`[concatenateClips] Concatenating ${clipPaths.length} clips`);

    // Verify all clips exist
    for (const clipPath of clipPaths) {
      if (!fs.existsSync(clipPath)) {
        throw new Error(`Clip not found: ${clipPath}`);
      }
    }

    // Create concat list file with absolute paths
    const concatList = clipPaths.map(p => `file '${path.resolve(p)}'`).join('\n');
    fs.writeFileSync(concatListPath, concatList);
    console.log(`[concatenateClips] Created concat list: ${concatListPath}`);

    return new Promise((resolve, reject) => {
      ffmpeg()
        .input(concatListPath)
        .inputOptions(['-f', 'concat', '-safe', '0'])
        .outputOptions([
          '-c', 'copy',
          '-movflags', '+faststart' // Optimize for web playback
        ])
        .output(outputPath)
        .on("end", () => {
          console.log(`[concatenateClips] Successfully created: ${path.basename(outputPath)}`);
          try {
            fs.unlinkSync(concatListPath);
          } catch (err) {
            console.warn(`[concatenateClips] Failed to cleanup concat list`);
          }
          resolve();
        })
        .on("error", (err) => {
          console.error(`[concatenateClips] Error:`, err.message);
          try {
            fs.unlinkSync(concatListPath);
          } catch (cleanupErr) {
            // Ignore cleanup errors
          }
          reject(err);
        })
        .on("stderr", (stderrLine) => {
          console.log(`[concatenateClips] FFmpeg: ${stderrLine}`);
        })
        .run();
    });
  }

