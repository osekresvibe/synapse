  static async addSubtitles(
    videoPath: string,
    transcription: string,
    style: "modern" | "classic" | "minimal" = "modern",
    position: "bottom" | "top" | "center" = "bottom"
  ): Promise<string> {
    const outputId = nanoid();
    const outputPath = path.join(VIDEOS_DIR, `subtitled-${outputId}.mp4`);
    const srtPath = path.join(VIDEOS_DIR, `subtitles-${outputId}.srt`);

    // Generate SRT file from transcription (simple word-based timing)
    const words = transcription.split(" ");
    const wordsPerSubtitle = 5;
    const durationPerSubtitle = 2; // seconds

    let srtContent = "";
    for (let i = 0; i < words.length; i += wordsPerSubtitle) {
      const index = Math.floor(i / wordsPerSubtitle) + 1;
      const startTime = (i / wordsPerSubtitle) * durationPerSubtitle;
      const endTime = startTime + durationPerSubtitle;
      const text = words.slice(i, i + wordsPerSubtitle).join(" ");

      srtContent += `${index}\n`;
      srtContent += `${this.formatSrtTime(startTime)} --> ${this.formatSrtTime(endTime)}\n`;
      srtContent += `${text}\n\n`;
    }

    fs.writeFileSync(srtPath, srtContent);

    // Subtitle styles
    const styles: Record<string, string> = {
      modern: "FontName=Arial,FontSize=24,PrimaryColour=&H00FFFFFF,OutlineColour=&H00000000,BackColour=&H80000000,Bold=1,Outline=2,Shadow=1",
      classic: "FontName=Times New Roman,FontSize=22,PrimaryColour=&H00FFFFFF,OutlineColour=&H00000000,Bold=0,Outline=1",
      minimal: "FontName=Helvetica,FontSize=20,PrimaryColour=&H00FFFFFF,BackColour=&H00000000,Bold=0,Outline=0",
    };

    const alignment = position === "bottom" ? 2 : position === "top" ? 8 : 5;
    const marginV = position === "bottom" ? 20 : position === "top" ? 20 : 0;

    return new Promise((resolve, reject) => {
      ffmpeg(videoPath)
        .outputOptions([
          `-vf subtitles=${srtPath}:force_style='${styles[style]},Alignment=${alignment},MarginV=${marginV}'`,
          '-preset', 'ultrafast',
          '-threads', '0',
          '-crf', '20'
        ])
        .output(outputPath)
        .on("end", () => {
          fs.unlinkSync(srtPath); // Clean up SRT file
          resolve(outputPath);
        })
        .on("error", reject)
        .run();
    });
  }

  // Helper to format time for SRT files
  private static formatSrtTime(seconds: number): string {
    const hours = Math.floor(seconds / 3600);
    const minutes = Math.floor((seconds % 3600) / 60);
    const secs = Math.floor(seconds % 60);
    const ms = Math.floor((seconds % 1) * 1000);

    return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(secs).padStart(2, '0')},${String(ms).padStart(3, '0')}`;
  }

